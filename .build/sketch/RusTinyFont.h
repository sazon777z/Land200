inline const Glyph *findGlyph(uint32_t codepoint) {
  for (const auto &glyph : kGlyphs) {
    if (glyph.codepoint == codepoint) {
      return &glyph;
    }
  }
  return nullptr;
}

inline uint32_t decodeNext(const String &text, int &index) {
  const uint8_t lead = static_cast<uint8_t>(text.charAt(index++));
  if ((lead & 0x80) == 0) {
    return lead;
  }

  if ((lead & 0xE0) == 0xC0 && index < text.length()) {
    const uint8_t tail = static_cast<uint8_t>(text.charAt(index++));
    return ((lead & 0x1F) << 6) | (tail & 0x3F);
  }

  if ((lead & 0xF0) == 0xE0 && (index + 1) < text.length()) {
    const uint8_t tail1 = static_cast<uint8_t>(text.charAt(index++));
    const uint8_t tail2 = static_cast<uint8_t>(text.charAt(index++));
    return ((lead & 0x0F) << 12) | ((tail1 & 0x3F) << 6) | (tail2 & 0x3F);
  }

  return '?';
}

inline int measureText(const String &text, int scale = 1) {
  int width = 0;
  int index = 0;
  while (index < text.length()) {
    const uint32_t codepoint = decodeNext(text, index);
    const Glyph *glyph = findGlyph(codepoint);
    width += (glyph != nullptr ? glyph->advance : kDefaultAdvance) * scale;
  }
  return width > 0 ? width - scale : 0;
}

inline void drawText(Arduino_GFX *target, int x, int y, const String &text,
                     uint16_t color, int scale = 1) {
  int cursorX = x;
  int index = 0;
  while (index < text.length()) {
    const uint32_t codepoint = decodeNext(text, index);
    const Glyph *glyph = findGlyph(codepoint);

    const uint8_t advance =
        (glyph != nullptr ? glyph->advance : kDefaultAdvance) * scale;

    if (glyph == nullptr) {
      cursorX += advance;
      continue;
    }

    for (uint8_t row = 0; row < kGlyphHeight; ++row) {
      for (uint8_t col = 0; col < kGlyphWidth; ++col) {
        if (glyph->rows[row] & (1 << (kGlyphWidth - 1 - col))) {
          target->fillRect(cursorX + col * scale, y + row * scale, scale, scale,
                           color);
        }
      }
    }

    cursorX += advance;
  }
}
